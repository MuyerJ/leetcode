public class Q36_有效数独 {
    static Set<Character> checkSet = new HashSet<>();

    public static void main(String[] args) {
        System.out.println(isValidSudoku(new char[][]{{'.', '.', '.', '.', '.', '.', '5', '.', '.'}, {'.', '.', '.', '.', '.', '.', '.', '.', '.'}, {'.', '.', '.', '.', '.', '.', '.', '.', '.'}, {'9', '3', '.', '.', '2', '.', '4', '.', '.'}, {'.', '.', '7', '.', '.', '.', '3', '.', '.'}, {'.', '.', '.', '.', '.', '.', '.', '.', '.'}, {'.', '.', '.', '3', '4', '.', '.', '.', '.'}, {'.', '.', '.', '.', '.', '3', '.', '.', '.'}, {'.', '.', '.', '.', '.', '5', '2', '.', '.'}}));
    }

    public static boolean isValidSudoku(char[][] board) {
        //校验行
        for (int i = 0; i < board.length; i++) {
            if (!checkOK(board, i, i, 0, board[i].length - 1)) {
                return false;
            }
        }
        //校验列
        for (int i = 0; i < board[0].length; i++) {
            if (!checkOK(board, 0, board.length - 1, i, i)) {
                return false;
            }
        }
        //校验一组
        int i = 0;
        while (i < 9) {
            int j = 0;
            while (j < 9) {
                System.out.println(i + "," + (i + 2) + "," + j + "," + (j + 2));
                if (!checkOK(board, i, i + 2, j, j + 2)) {
                    return false;
                }
                j += 3;
            }
            i += 3;
        }
        return true;
    }

    public static boolean checkOK(char[][] board, int colStart, int colEnd, int rowStart, int rowEnd) {
        checkSet.clear();
        int size = 0;
        for (int i = colStart; i <= colEnd; i++) {
            for (int j = rowStart; j <= rowEnd; j++) {
                if (board[i][j] != '.') {
                    size++;
                    checkSet.add(board[i][j]);
                }
            }
        }
        return size == checkSet.size();
    }
}

